let level = 1; 
// G√úNCELLENMƒ∞≈û KART SAYILARI: 12 (4x3), 16 (4x4), 20 (4x5)
const LEVELS = [12, 16, 20]; 
let gameStage = 'SELECTION'; // 'SELECTION' veya 'PLAY'
let selectedBombs = []; // Kendi se√ßtiƒüimiz bombalarƒ±n indexleri

let gameData = {
    board: [], 
    turn: 0,   // 0 = Host, 1 = Guest
    hostLives: 2,
    guestLives: 2,
    cardsLeft: 0,
    hostBombs: [], 
    guestBombs: [],
    isGameOver: false
};

