export function showScreen(screenId) {
     Object.values(screens).forEach(screen => screen.classList.remove('active'));
    screens[screenId].classList.add('active');
}

export function showGlobalMessage(message, isError = true) {
    const globalMessage = document.getElementById('globalMessage');
    const globalMessageText = document.getElementById('globalMessageText');
    globalMessageText.textContent = message;
    globalMessage.classList.remove('bg-red-600', 'bg-green-600');
    globalMessage.classList.add(isError ? 'bg-red-600' : 'bg-green-600');
    globalMessage.classList.remove('hidden');
    globalMessage.classList.add('show');
    setTimeout(() => { globalMessage.classList.add('hidden'); globalMessage.classList.remove('show'); }, 4000);
}

// --- OYUN MANTIƒûI VE √áƒ∞Zƒ∞M ---

function initializeGame(initialBoardSize) {
    gameData.board = Array(initialBoardSize).fill(null).map(() => ({
        opened: false,
        content: '?',
        isBomb: false
    }));
    gameData.cardsLeft = initialBoardSize;
    gameData.hostLives = 2;
    gameData.guestLives = 2;
    gameData.hostBombs = [];
    gameData.guestBombs = [];
    selectedBombs = [];
    gameData.turn = 0;
    gameData.isGameOver = false;
    gameStage = 'SELECTION'; 
}

function drawBoard() {
    const boardSize = LEVELS[level - 1];
    
    // Grid d√ºzenini sadece 4 s√ºtun (4 a≈üaƒüƒ± inme) olarak ayarla
    gameBoardEl.className = 'grid w-full max-w-sm mx-auto memory-board'; 
    gameBoardEl.style.gridTemplateColumns = 'repeat(4, 1fr)'; // 4 s√ºtun (4x3, 4x4, 4x5 i√ßin)
    
    gameBoardEl.innerHTML = '';
    
    gameData.board.forEach((cardState, index) => {
        const cardContainer = document.createElement('div');
        cardContainer.className = 'card-container aspect-square';

        const card = document.createElement('div');
        card.className = `card cursor-pointer`;
        card.dataset.index = index;

        const front = document.createElement('div');
        front.className = 'card-face front'; // Sizin stilinize g√∂re front/back
        front.textContent = '?';
        
        const back = document.createElement('div');
        back.className = 'card-face back';
        back.textContent = cardState.content;

        card.appendChild(front);
        card.appendChild(back);
        cardContainer.appendChild(card);
        
        if (cardState.opened) {
            card.classList.add('flipped');
        } else {
            // SADECE SE√áEN Kƒ∞≈ûƒ∞Nƒ∞N G√ñRMESƒ∞ ƒ∞√áƒ∞N KIRMIZILIK
            if (gameStage === 'SELECTION' && selectedBombs.includes(index)) {
                card.classList.add('bomb-selected'); 
            }
            
            // KRƒ∞Tƒ∞K D√úZELTME: TIKLAMA OLAYINI CARD-CONTAINER'A EKLE!
            cardContainer.addEventListener('click', handleCardClick);
        }
        
        gameBoardEl.appendChild(cardContainer);
    });
    updateStatusDisplay();
}

function updateStatusDisplay() {
    const myLives = isHost ? gameData.hostLives : gameData.guestLives;
    const opponentLives = isHost ? gameData.guestLives : gameData.hostLives;
    
    myLivesEl.textContent = '‚ù§Ô∏è'.repeat(Math.max(0, myLives));
    opponentLivesEl.textContent = '‚ù§Ô∏è'.repeat(Math.max(0, opponentLives));

    const isMyTurn = (isHost && gameData.turn === 0) || (!isHost && gameData.turn === 1);

    if (gameStage === 'SELECTION') {
        if (selectedBombs.length < 3) {
            turnStatusEl.textContent = `üí£ BOMBA SE√á: ${selectedBombs.length} / 3`;
            actionMessageEl.textContent = "Rakibinizin ba≈üacaƒüƒ± 3 bombayƒ± kartlar √ºzerine yerle≈ütirin!";
            turnStatusEl.classList.remove('text-red-600');
            turnStatusEl.classList.add('text-green-600');
        } else {
            turnStatusEl.textContent = `‚è≥ RAKIP SE√áƒ∞YOR...`;
            actionMessageEl.textContent = "Se√ßiminiz tamamlandƒ±! Rakibinizin bomba se√ßmesini bekleyin.";
            turnStatusEl.classList.remove('text-green-600');
            turnStatusEl.classList.add('text-red-600');
        }
    } else if (gameStage === 'PLAY') {
        if (isMyTurn) {
            turnStatusEl.textContent = '‚úÖ SIRA SENDE!';
            actionMessageEl.textContent = "Bir kart a√ß! Rakibinizin bombalarƒ±ndan ka√ßƒ±nmaya √ßalƒ±≈üƒ±n.";
            turnStatusEl.classList.remove('text-red-600');
            turnStatusEl.classList.add('text-green-600');
        } else {
            turnStatusEl.textContent = '‚è≥ RAKƒ∞Bƒ∞N SIRASI';
            actionMessageEl.textContent = "Rakibinizin hamlesini bekleyin...";
            turnStatusEl.classList.remove('text-green-600');
            turnStatusEl.classList.add('text-red-600');
        }
    }
    
    if (gameData.isGameOver) {
        turnStatusEl.textContent = "‚úÖ OYUN Bƒ∞TTƒ∞!";
        actionMessageEl.textContent = "Sonu√ßlar hesaplanƒ±yor...";
    }
}

// --- ANIMASYON VE SES ---

async function triggerWaitAndVibrate() {
     if (gameData.cardsLeft < 8 && gameStage === 'PLAY') { 
        startVibration();
        await new Promise(resolve => setTimeout(resolve, 2000));
        stopVibration();
    }
}
function startVibration() {
    const cardContainers = gameBoardEl.querySelectorAll('.card-container');
    cardContainers.forEach(container => {
        const card = container.querySelector('.card');
        if (card && !card.classList.contains('flipped')) {
            card.classList.add('vibrate');
        }
    });
    playSound(audioWait);
}

function stopVibration() {
    const cardContainers = gameBoardEl.querySelectorAll('.card-container');
    cardContainers.forEach(container => {
        const card = container.querySelector('.card');
        if (card) {
            card.classList.remove('vibrate');
        }
    });
    audioWait.pause();
    audioWait.currentTime = 0;
}


// --- HAREKET ƒ∞≈ûLEYƒ∞Cƒ∞LERƒ∞ ---

function handleCardClick(event) {
    // Tƒ±klama olayƒ±nƒ± ba≈ülatan card-container'ƒ± bul
    const cardContainer = event.currentTarget; 
    // ƒ∞√ßindeki asƒ±l .card elementini bul
    const cardElement = cardContainer.querySelector('.card');
    
    // Eƒüer card elementi zaten a√ßƒ±lmƒ±≈üsa veya bulunamazsa dur.
    if (!cardElement || cardElement.classList.contains('flipped')) return; 
    
    const cardIndex = parseInt(cardElement.dataset.index);

    if (gameStage === 'SELECTION') {
        if (selectedBombs.includes(cardIndex)) {
            selectedBombs = selectedBombs.filter(i => i !== cardIndex);
        } else if (selectedBombs.length < 3) {
            selectedBombs.push(cardIndex);
        }
        drawBoard(); 
        
        if (selectedBombs.length === 3) {
            // Bombalarƒ± sunucuya g√∂nder
            socket.emit('bombSelectionComplete', { roomCode: currentRoomCode, isHost: isHost, bombs: selectedBombs });
            updateStatusDisplay();
        }
    } else if (gameStage === 'PLAY') {
        const isMyTurn = (isHost && gameData.turn === 0) || (!isHost && gameData.turn === 1);
        if (!isMyTurn || gameData.isGameOver) return; 
        
        sendMove(cardIndex);
    }
}

function sendMove(index) {
    if (socket && socket.connected) {
        socket.emit('gameData', {
            roomCode: currentRoomCode,
            type: 'MOVE',
            cardIndex: index,
        });
    }
}

async function applyMove(index) {
    if (gameData.board[index].opened) return;

    // Hamleyi yapan oyuncuya g√∂re bombayƒ± kontrol et
    const currentPlayerIsHost = gameData.turn === 0;
    const opponentBombs = currentPlayerIsHost ? gameData.guestBombs : gameData.hostBombs;
    const hitBomb = opponentBombs.includes(index);
    
    await triggerWaitAndVibrate();

    gameData.board[index].opened = true;
    gameData.cardsLeft -= 1;
    
    if (hitBomb) {
        gameData.board[index].content = 'üí£';
        // Hamle yapan oyuncu can kaybeder
        if (currentPlayerIsHost) {
            gameData.hostLives--;
        } else { 
            gameData.guestLives--;
        }
        
        playSound(audioBomb);
        showGlobalMessage(`BOOM! ${currentPlayerIsHost ? 'Host' : 'Guest'} bombaya bastƒ±!`, true);
    } else {
        gameData.board[index].content = EMOTICONS[Math.floor(Math.random() * EMOTICONS.length)];
        playSound(audioEmoji);
    }
    
    drawBoard(); 
    
    setTimeout(() => {
        // Sƒ±rayƒ± deƒüi≈ütir
        gameData.turn = gameData.turn === 0 ? 1 : 0;
        updateStatusDisplay();
        
        // Oyun biti≈üini kontrol et
        if (gameData.hostLives <= 0 || gameData.guestLives <= 0) {
            const winner = (gameData.hostLives <= 0 && gameData.guestLives <= 0) ? 'DRAW' : (gameData.hostLives <= 0 ? 'Guest' : 'Host');
            endGame(winner);
        }
        
    }, 1000);
}

function endGame(winnerRole) {
    gameData.isGameOver = true;
    gameStage = 'ENDED';
    
    const myRole = isHost ? 'Host' : 'Guest';
    const iWon = (winnerRole === myRole);
    const isDraw = (winnerRole === 'DRAW');
    
    if (isDraw) {
        turnStatusEl.textContent = `ü§ù BERABERLƒ∞K!`;
        actionMessageEl.textContent = `Her iki oyuncu da t√ºm canlarƒ±nƒ± kaybetti!`;
        showGlobalMessage('ü§ù Beraberlik! Her ikiniz de harika oynadƒ±nƒ±z!', false);
    } else if (iWon) {
        turnStatusEl.textContent = `üéâ KAZANDIN!`;
        actionMessageEl.textContent = `Tebrikler! Rakibinizi yendiniz!`;
        showGlobalMessage('üéâ Tebrikler! Bu turu kazandƒ±nƒ±z!', false);
    } else {
        turnStatusEl.textContent = `üòî KAYBETTƒ∞N`;
        actionMessageEl.textContent = `Rakibiniz bu turu kazandƒ±.`;
        showGlobalMessage('üòî Bu turu kaybettiniz. Bir sonrakinde daha dikkatli olun!', true);
    }
    
    setTimeout(() => {
        if (level < LEVELS.length) {
            level++;
            showGlobalMessage(`üéÆ Seviye ${level} Ba≈ülƒ±yor! (${LEVELS[level-1]} Kart)`, false);
            
            // Sadece Host, yeni seviye sinyalini g√∂nderir.
            if (isHost) {
                socket.emit('nextLevel', { roomCode: currentRoomCode, newLevel: level });
            }
            // T√ºm oyuncular initializeGame'i √ßaƒüƒ±rƒ±r (ya sinyalle ya da kendisi).
            initializeGame(LEVELS[level - 1]);
            drawBoard();
            updateStatusDisplay();
        } else {
             showGlobalMessage("üèÜ T√ºm seviyeler tamamlandƒ±! Harika oyund!", false);
             setTimeout(() => resetGame(), 2000);
        }
    }, 4000);
}

// --- SOCKET.IO ƒ∞√áƒ∞N SETUP FONKSƒ∞YONU ---
export function setupSocketHandlers(s, roomCode, host, opponentNameFromIndex) {
    socket = s;
    currentRoomCode = roomCode;
    isHost = host;
    opponentName = opponentNameFromIndex;
    
    opponentNameEl.textContent = opponentName;
    roleStatusEl.textContent = isHost ? "üéÆ Rol: HOST (Sen ba≈üla)" : "üéÆ Rol: GUEST (Rakip ba≈ülar)";

    // Oyun ba≈ülatƒ±lƒ±yor
    level = 1; // Yeni oyuna ba≈ülarken seviyeyi 1'e sƒ±fƒ±rla
    initializeGame(LEVELS[level - 1]);
    drawBoard();
    showScreen('game');
    showGlobalMessage(`üéÆ Oyun ${opponentName} ile ba≈üladƒ±! üí£ √ñnce bomba se√ßimi yapƒ±n.`, false);
    
    // --- SOCKET.IO ƒ∞≈ûLEYƒ∞Cƒ∞LERƒ∞ ---

    // Bomb Se√ßimi Tamamlandƒ±
    socket.on('bombSelectionComplete', ({ isHost: selectionHost, bombs }) => {
        if (selectionHost) {
            gameData.hostBombs = bombs;
        } else {
            gameData.guestBombs = bombs;
        }
        
        if (gameData.hostBombs.length === 3 && gameData.guestBombs.length === 3) {
            gameStage = 'PLAY';
            showGlobalMessage('üöÄ Her iki oyuncu da hazƒ±r! Kart a√ßma a≈üamasƒ± ba≈ülƒ±yor!', false);
            // Sadece bir oyuncu (HOST) ba≈ülar (Turn = 0)
            gameData.turn = 0; 
            drawBoard(); 
        } else {
            actionMessageEl.textContent = "Rakip bombasƒ±nƒ± se√ßti. ≈ûimdi siz de 3 bomba se√ßin!";
        }
        updateStatusDisplay();
    });

    // gameData Olayƒ± (Hamle Geldi - Kendi veya Rakip)
    socket.on('gameData', (data) => {
        if (gameStage !== 'PLAY') return;
        
        if (data.type === 'MOVE') {
            // Server tarafƒ±ndan onaylanmƒ±≈ü hamleyi uygula
            applyMove(data.cardIndex); 
        }
    });

    // Hata mesajlarƒ± i√ßin dinleyici
    socket.on('error', (message) => {
        showGlobalMessage(message, true);
    });

    // Seviye Atlama Sinyali
    socket.on('nextLevel', ({ newLevel }) => {
        level = newLevel;
        showGlobalMessage(`üéÜ Seviye ${level} - ${LEVELS[level-1]} Kart! Yeni bomba se√ßimi ba≈ülatƒ±lƒ±yor...`, false);
        initializeGame(LEVELS[level - 1]);
        drawBoard();
        updateStatusDisplay();
    });
    
    // Rakip Ayrƒ±ldƒ±
    socket.on('opponentLeft', (message) => {
        showGlobalMessage(message || 'Rakibiniz ayrƒ±ldƒ±. Lobiye d√∂n√ºl√ºyor.', true);
        resetGame();
    });
}

export function resetGame() {
    // T√ºm oyun ayarlarƒ±nƒ± sƒ±fƒ±rlar ve lobiye d√∂ner (En g√ºvenli yol: Sayfayƒ± yenilemek)
    window.location.reload(); 
}

